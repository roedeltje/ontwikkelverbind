// Account verwijderen
export const deleteAccount = () => dispatch => {
  if (
    window.confirm("Weet je het zeker? Dit kan niet meer teruggedraaid worden!")
  ) {
    axios
      .delete("/api/profile")
      .then(res =>
        dispatch({
          type: SET_CURRENT_USER,
          payload: {}
        })
      )
      .catch(err =>
        dispatch({
          type: GET_ERRORS,
          payload: err.response.data
        })
      );
  }
};

// Profiel loading
export const setProfileLoading = () => {
  return {
    type: PROFILE_LOADING
  };
};

// Profiel leegmaken
export const clearCurrentProfile = () => {
  return {
    type: CLEAR_CURRENT_PROFILE
  };
};
